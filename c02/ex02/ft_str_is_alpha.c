int	ft_str_is_alpha(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i])
	{
		if (!(str[i] >= 0x41 && str[i] <= 0x5A)
			&& !(str[i] >= 0x61 && str[i] <= 0x7A))
			return (0);
		i++;
	}
	return (1);
}
