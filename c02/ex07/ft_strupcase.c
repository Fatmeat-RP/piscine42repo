char	*ft_strupcase(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 0x61 && str[i] <= 0x7A)
			str[i] -= 32;
		i++;
	}
	return (str);
}
