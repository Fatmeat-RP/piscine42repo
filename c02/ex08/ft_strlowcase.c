char	*ft_strlowcase(char *str)
{
	unsigned int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 0x41 && str[i] <= 0x5A)
			str[i] += 32;
		i++;
	}
	return (str);
}
