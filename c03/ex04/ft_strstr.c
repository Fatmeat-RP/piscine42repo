char	*ft_strstr(char *str, char *to_find)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	if (to_find[j] == 0x00)
		return (str);
	while (str[i])
	{
		if (str[i] == to_find[j])
		{
			while (str[i + j] == to_find[j])
			{
				if (!to_find[j + 1])
					return (str + i);
				j++;
			}
		}
		i++;
		j = 0;
	}
	return (0);
}

